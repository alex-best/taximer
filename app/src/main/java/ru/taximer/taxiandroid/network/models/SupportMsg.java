package ru.taximer.taxiandroid.network.models;

public class SupportMsg {
    String text;
    String email;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
